package com.silentcircle.common.widget;

import android.annotation.TargetApi;
import android.content.Context;
import android.os.Build;
import android.util.AttributeSet;
import android.view.View;

/**
 * Created by petrakeas on 12/08/16.
 */
public class ViewHeightNotifier extends View {

    public interface HeightListener {
        public void onHeightChanged(int viewHeight);
    }

    private int mHeight;
    private HeightListener mListener;

    public void setHeightListener(HeightListener listener) {
        this.mListener = listener;
    }

    public ViewHeightNotifier(Context context) {
        super(context);
    }

    public ViewHeightNotifier(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    public ViewHeightNotifier(Context context, AttributeSet attrs, int defStyleAttr) {
        super(context, attrs, defStyleAttr);
    }

    @TargetApi(Build.VERSION_CODES.LOLLIPOP)
    public ViewHeightNotifier(Context context, AttributeSet attrs, int defStyleAttr, int defStyleRes) {
        super(context, attrs, defStyleAttr, defStyleRes);
    }

    @Override
    protected void onSizeChanged(int w, int h, int oldw, int oldh) {
        super.onSizeChanged(w, h, oldw, oldh);
        notifyHeightChanged(h);
    }

    private void notifyHeightChanged(int height) {
        if (height == mHeight) {
            return;
        }
        mHeight = height;
        if (mListener == null) {
            return;
        }
        mListener.onHeightChanged(height);
    }
}
