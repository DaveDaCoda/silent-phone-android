package com.silentcircle.messaging.views.adapters;

import android.content.Context;
import android.graphics.Canvas;
import android.graphics.drawable.Drawable;
import android.support.v4.content.ContextCompat;
import android.support.v7.widget.RecyclerView;
import android.view.View;

import com.silentcircle.silentphone2.R;

/**
 * Item divider for recycler view with option to set left and right margins.
 */
public class PaddedDividerItemDecoration extends RecyclerView.ItemDecoration {

    private Drawable mDivider;

    private int mMarginLeft;
    private int mMarginRight;

    public PaddedDividerItemDecoration(Context context) {
        mDivider = ContextCompat.getDrawable(context, R.drawable.bg_item_divider);
        mDivider.setAlpha(64);
    }

    public void setMargins(int marginLeft, int marginRight) {
        mMarginLeft = marginLeft;
        mMarginRight = marginRight;
    }

    public void onDrawOver(Canvas canvas, RecyclerView parent, RecyclerView.State state) {
        int left = parent.getPaddingLeft() + mMarginLeft;
        int right = parent.getWidth() - parent.getPaddingRight() - mMarginRight;

        int childCount = parent.getChildCount();
        for (int i = 0; i < childCount; i++) {
            View child = parent.getChildAt(i);

            RecyclerView.LayoutParams params = (RecyclerView.LayoutParams) child.getLayoutParams();

            int top = child.getBottom() + params.bottomMargin;
            int bottom = top + mDivider.getIntrinsicHeight();

            mDivider.setBounds(left, top, right, bottom);
            mDivider.draw(canvas);
        }
    }
}
